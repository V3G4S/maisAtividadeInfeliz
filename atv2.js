
let num1 = 200.456
let num2 = 150.789

let num1Arredondado = Math.round(num1)
console.log(num1Arredondado)

let maxValor = Math.max(num1, num2)
let minValor = Math.min(num1, num2)
console.log("Máximo:", maxValor)
console.log("Mínimo:", minValor)

let raizQuadrada = Math.sqrt(num2)
console.log(raizQuadrada)

let num1Cientifico = num1.toExponential()
console.log(num1Cientifico)
